function Pregunta1()
{
  var Palabra="";
  for (var i = 1; i < 6; i++) {
    Palabra+=document.getElementById('Letra'+i).value;
  }
  if (Palabra.toLowerCase()=="viaje") {
    alert("La Palabra Digitada Es Correcta");
    return window.open("Pregunta2.html");
  }
  else {
    alert("La Palabra Digitada Es Incorrecta");
  }

}
